-- ========================================
--            PROBLEM
-- ========================================
-- Find tweets where the length of content
-- is strictly greater than 15 characters.
--
-- Return only tweet_id.


-- ========================================
--            SOLUTION
-- ========================================

SELECT
    tweet_id
FROM Tweets
WHERE LENGTH(content) > 15;


